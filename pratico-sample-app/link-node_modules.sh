#!/usr/bin/env bash

if ! [ -s node_modules ]; then
  echo "não existe link simbólico para a pasta node_modules! Criando..."

  ln -s ../pratico-library/node_modules node_modules

  cd ../pratico-library

  yarn prepublish

  yarn link

  cd ../pratico-sample-app

  yarn link ngx-pratico
else
   echo 'modules already linked'
fi

